C = float(input('Insira sua temperatura em Celsius: '))
Far = (C * 9 / 5) + 32
print(f'Logo, se ua temperatura, em Celsius ,é {C}; \n'
      f'Em Fahrenheit se torna {Far}.')
Kel = C + 273.15
print(f'considerando que tenhamos {C} graus C°,{Far} graus Fahrenheit; \n'
      f'Esses dois equivalem á {Kel} graus Kelvin.  f')
